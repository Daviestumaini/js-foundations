/* An operator is a special symbol that tells the computer to perform a specific operation.
In JavaScript, there are several types of operators, including:
1. Arithmetic Operators: These operators are used to perform mathematical operations such as addition, subtraction, multiplication, division, and modulus.
   - Example:
    let a = 10;
    let b = 5;
    console.log(a + b); // Output: 15
    console.log(a - b); // Output: 5
    console.log(a * b); // Output: 50
    console.log(a / b); // Output: 2
    console.log(a % b); // Output: 0
2. Assignment Operators: These operators are used to assign values to variables.
   - Example:
    let x = 10;
    x += 5; // Equivalent to x = x + 5
    console.log(x); // Output: 15
3. Comparison Operators: These operators are used to compare two values and return a boolean result.
   - Example:
    let c = 10;
    let d = 5;
    console.log(c > d); // Output: true
    console.log(c < d); // Output: false
    console.log(c == d); // Output: false
    console.log(c != d); // Output: true
4. Logical Operators: These operators are used to combine multiple boolean expressions.
   - Example:   let isStudent = true;
    let hasID = false;
    console.log(isStudent && hasID); // Output: false
    console.log(isStudent || hasID); // Output: true
    console.log(!isStudent); // Output: false
In the real world, operators are like tools in a toolbox. Just as you would use a hammer to drive nails and a screwdriver to turn screws, in programming, you use different operators to perform various operations on data. Understanding how to use operators effectively is essential for writing efficient and functional code.
So, just as you wouldn't use a hammer to turn a screw, in programming, you wouldn't use an arithmetic operator to compare values or a logical operator to assign values. Each operator has its specific purpose, and using them correctly is crucial for the success of your code.*/